package ge.tsu.bean;

public class Step {

	private String lat;
	private String lot;
	
	
	public Step(String lat, String lot) {
		super();
		this.lat = lat;
		this.lot = lot;
	}
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	public String getLot() {
		return lot;
	}
	public void setLot(String lot) {
		this.lot = lot;
	}
	
	
	
}
